const add = (num01, num02) => {
  const add = num01 + num02;
  return add;
};
const minus = (num01, num02) => {
  const minus = num01 - num02;
  return minus;
};
const multiply = (num01, num02) => {
  const multiply = num01 * num02;
  return multiply;
};
const divide = (num01, num02) => {
  const divide = num01 / num02;
  return divide;
};

export { add, minus, multiply, divide };
